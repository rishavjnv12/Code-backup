struct node{
    int val;
    node* next;
};
node* removeDuplicate(node * head){
    node *dummy =new node();
    dummy->next=head;
    node*prev=dummy;
    node*current=head;
    while(current){
        while(current->next and prev->next->val==current->next->val){
            //node*temp=current;
            current=current->next;
            //delete temp;
        }
        if(prev->next==current)
            prev=prev->next;
        else{
            prev->next=current->next;
        }
        current=current->next;
    }
    head=dummy->next;
    delete dummy;
    return head;
}
node* insert(node * head,int n){
    if(head==nullptr){
        node* tmp=new node();
        tmp->val=n;
        tmp->next=nullptr;
        return tmp;
    }else{
        node* tmp=head;
        while(tmp->next){
            tmp=tmp->next;
        }
        node* tmp2=new node();
        tmp2->val=n;
        tmp2->next=nullptr;
        tmp->next=tmp2;
    }
    return head;
}
